func Bytes32ToString(b [32]byte) string {
	n := bytes.IndexByte(b[:], 0) // ‡∏´‡∏≤‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á Null byte ‡πÅ‡∏£‡∏Å
	if n == -1 {
		n = len(b)
	}
	return string(b[:n])
}
